// 산술 연산자
console.log(5 * 4); // 20

// 문자열 연결 결합자
console.log('My name is ' + 'Lee'); //'My name is Lee'

// 할당 연산자 -> 'red'
let color = 'red';
console.log(color);

// 비교 연산자
let bool = 3 > 5;
console.log(bool);

// 논리 연산자
let bool2 = true && false;
console.log(bool2);

// 타입 연산자
console.log(typeof 'Hi'); // String
